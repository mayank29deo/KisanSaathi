/**
 * Get all districts for a given state
 * @param {string} state
 * @returns {string[]}
 */
export function getDistricts(state) {
  return Object.keys(SOIL_DATA[state]?.districts || {});
}

/**
 * Get all states
 * @returns {string[]}
 */
export function getStates() {
  return Object.keys(SOIL_DATA);
}

/**
 * Get pre-filled soil & climate parameters for a state/district/season combination
 * @param {string} state
 * @param {string} district
 * @param {string} season  — "Kharif" | "Rabi" | "Zaid"
 * @returns {{ N, P, K, pH, rainfall, soil_moisture, min_temp, max_temp } | null}
 */
export function getSoilParams(state, district, season) {
  const entry = SOIL_DATA[state]?.districts?.[district];
  if (!entry) return null;
  const temps = entry.temps?.[season] || { min_temp: 20, max_temp: 35 };
  return {
    N:             entry.N,
    P:             entry.P,
    K:             entry.K,
    pH:            entry.pH,
    rainfall:      entry.rainfall,
    soil_moisture: entry.soil_moisture,
    min_temp:      temps.min_temp,
    max_temp:      temps.max_temp,
  };
}

